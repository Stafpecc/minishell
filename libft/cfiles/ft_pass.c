#include "libft.h"

int	ft_pass(int i, const char *str, int *s)
{
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			*s = -1;
		i++;
	}
	return (i);
}
